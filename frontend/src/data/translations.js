export function getText(language, scope, key, fallback) {
  const localized = TEXTS[language]?.[scope]?.[key];
  if (localized != null && localized !== "") return localized;
  if (language === "fr") {
    return TEXTS.fr?.[scope]?.[key] || TEXTS.universal?.[scope]?.[key] || fallback;
  }
  const enVal = TEXTS.en?.[scope]?.[key];
  if (enVal != null && enVal !== "") return enVal;
  const uniVal = TEXTS.universal?.[scope]?.[key];
  if (uniVal != null && uniVal !== "") return uniVal;
  return fallback;
}

export function getQuizQuestionText(language, questionId) {
  const q = TEXTS[language]?.quiz?.questions?.[questionId];
  if (q) return q;
  if (language === "fr") {
    return TEXTS.fr?.quiz?.questions?.[questionId] || TEXTS.universal?.quiz?.questions?.[questionId] || null;
  }
  return (
    TEXTS.en?.quiz?.questions?.[questionId] ||
    TEXTS.universal?.quiz?.questions?.[questionId] ||
    null
  );
}

export function getQuizChoiceText(language, questionId, choiceValue, fallback = {}) {
  const pick = (bundle) => bundle?.quiz?.choices?.[questionId]?.[choiceValue];
  const localized = pick(TEXTS[language]);
  if (localized) return localized;
  if (language === "fr") {
    return pick(TEXTS.universal) || fallback;
  }
  return pick(TEXTS.en) || pick(TEXTS.universal) || fallback;
}
